/**
 * @param {{ command: string, resolvedFamilies: Array<Record<string, unknown>>, state: Record<string, unknown> }} options
 * @returns {Record<string, unknown>}
 */
export function buildCliOutput({ command, resolvedFamilies, state }) {
  const output = {
    command,
    status: "implemented",
  };

  for (const [outputField, stateField] of CLI_OUTPUT_FIELD_MAPPINGS) {
    output[outputField] = state[stateField];
  }

  output.contract_families = resolvedFamilies;
  output.command_catalog_alignment = "docs/architecture/14-cli-command-catalog.md";
  return /** @type {Record<string, unknown>} */ (
    redactSensitiveValue(output, {
      enabled: true,
      secretValues: parseRedactionSecretList(process.env.AOR_REDACTION_SECRETS),
    })
  );
}
